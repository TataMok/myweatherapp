function dayTimeToday(dtt) {
  let days = [
    "Sunday",
    "Monday",
    "Tuesday",
    "Wednesday",
    "Thursday",
    "Friday",
    "Saturday",
  ];

  let day = days[dtt.getDay()];
  let hour = dtt.getHours();
  let minutes = dtt.getMinutes();
  if (hour < 10) {
    hour = `0${hour}`;
  }
  if (minutes < 10) {
    minutes = `0${minutes}`;
  }

  return `${day} ${hour}:${minutes}`;
}
let now = new Date();
let displaydtt = document.querySelector("#time");
displaydtt.innerHTML = dayTimeToday(now);

